    </h2>
    <br>
    <ul>
    	<li>Must be physically and mentally fit to operate a motor vehicle</li>
        <li>Must know how to read and write in Fust ilipino or English</li>
        <li>Must be clean, neat, and presentable</li>
        <li>Must not be an alcoholic nor a drug user</li>
    </ul>
    <br>
    <h3>Student's Permit Requirements</h3>
    <h4>Must be at least 16 years of age:</h4>
    <ol>
    	<li>Accomplished Application Form</li>
        <li>Proof of identity: NSO authenticated Birth Certificate, valid Passport , any legal or government issued document to prove age and identify (SSS ID, GSIS ID, TIN ID, Pag-ibig ID)</li>
        <li>Notarized letter of Parental/Guardian consent (16-18 yo)</li>
    </ol>
    <br>
    <h4>Additional Requirements for Foreigners:</h4>
    <ol>
    	<li>Original and photocopy of alien Certificates of Registration (ACR)</li>
        <li>Original and photocopy of valid Visa/Passport and any of the following:</li>
        <li>Proof of stay in the country for at least (1) month and has a permit to stay in the country for at least (5) months</li>
        <li>Employer Contract that certifies stay in the Philippines for at least (5) months</li>
        <li>Additional Requirements for Minor Foreigners (16-18 yrs. Old)
        	<ol>
                <li>Notarized letter of Parental/Guardian consent</li>
                <li>Original and photocopy of Alien Certificate of Registration (ACR)</li>
                <li>Original and photocopy of valid Visa/Passport and any of the following:</li>
                <li>Proof of stay of the country for at least (1) month and has a permit to stay in the country for at least (5) months</li>
                <li>Employer Contract that certifies stay in the Philippines for at least (5) months</li>
			</ol>
        </li>
        <li>Students School ID</li>
    </ol>
    <br>
